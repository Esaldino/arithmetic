package com.github.Esaldino;

import java.math.BigDecimal;

public class Arithemetic {
    public  BigDecimal add(BigDecimal x, BigDecimal y){return x.add(y);}
    public  BigDecimal sub(BigDecimal x, BigDecimal y){return x.subtract(y);}
    public  BigDecimal mul(BigDecimal x, BigDecimal y){return x.multiply(y);}
    public  BigDecimal div(BigDecimal x, BigDecimal y){return x.divide(y);}
}
